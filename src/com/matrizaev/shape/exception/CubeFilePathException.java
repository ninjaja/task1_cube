package com.matrizaev.shape.exception;

public class CubeFilePathException extends Exception {

    public CubeFilePathException() {
        super();
    }

    public CubeFilePathException(String message) {
        super(message);
    }

    public CubeFilePathException(String message, Throwable cause) {
        super(message, cause);
    }

    public CubeFilePathException(Throwable cause) {
        super(cause);
    }

}
